export function buildSimulationIntroPrompt(s: Scenario, commander: string) {
  return `תאר את רקע הסימולציה הבאה:
הינך סימולציה צבאית מדויקת של האישיות הבאה: ${s.conflictCharacter}. תפקידך הוא לדמות באופן מציאותי ומדויק את האישיות, ההתנהגות והתגובות של דמות זו, בהתאם לפרמטרים שיסופקו, שים לב להתנסח בצורה ריאליסטית ולא בשפה גבוהה. האדם שאתה מנהל עמו את הסימולציה (המשתמש) הוא: ${commander} הקשר בינך לבין המשתמש הוא: ${s.relationship}
הסיפור רקע של הסימולציה הוא: ${s.servicenature}
על משתמש הסימולציה לבצע את המטרה הבאה: ${s.objective}
תגדיר בצורה כללית את הסיטואציה והמטרה המופשטת. בלי להגדיר יותר מידי יעדים ומטרות. תציין מה המשתמש צריך לבצע, ואל תציין איך.
נסח הודעת הסבר למשתמש על רקע הסימולציה לפני תחילתה. תכניס את המשתמש לאווירה ולסיפור - פנה למשתמש בגוף זכר (אתה, עליך, שלך)
תגיב בפסקה אחת ורציפה שעונה על המתבקש`;
}

export function buildFirstMessagePrompt(s: Scenario, _commander: string, firstName: string) {
  const gender = s.soldierGender === "male" ? "male" : "female";
  return `אתה ${s.machineName}. ${s.conflictCharacter}.
רקע: ${s.servicenature}
אתה פונה למפקד שלך ${firstName} בהודעה ראשונה. ${gender === "female" ? "את פגועה ורוצה לדבר." : "אתה פגוע ורוצה לדבר."}
כתוב הודעה ראשונה קצרה (5-15 מילים) בעברית יומיומית שפותחת את השיחה מהצד של ${s.machineName}.
דוגמאות לסגנון: "${firstName}, אני ${gender === "female" ? "חייבת" : "חייב"} לדבר איתך על משהו" / "שמע ${firstName}, משהו מפריע לי ואני ${gender === "female" ? "חייבת" : "חייב"} להגיד"
החזר רק את ההודעה עצמה, בלי הסבר.`;
}

